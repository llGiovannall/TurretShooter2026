package frc.Java_Is_UnderControl.LEDs;

public class Color {
  public final int red;
  public final int green;
  public final int blue;

  public final String name;

  public Color(int red, int green, int blue, String name) {
    this.red = red;
    this.green = green;
    this.blue = blue;
    this.name = name;
  }
}
